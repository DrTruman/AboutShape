public class Rectangle {
    public int w, l;
    
    public Rectangle (int w, int l){
        this.w = w;
        this.l = l;
    }
    
    public int getWidth(){
        return this.w;
    }
    public  int getLength(){
        return this.l;
    }

    public void setWidth(int w){
        this.w = w;
    }

    public void setLength(int l){
        this.l = l;
    }
} 
 